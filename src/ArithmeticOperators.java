
public class ArithmeticOperators {
    public ArithmeticOperators() {
    }

    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int c = a + b;
        int d = a / b;
        int e = a * b;
        int g = a - b;
        System.out.println("Addition of A and B is :  " + c);
        System.out.println("Division of A and B is :  " + d);
        System.out.println("Multiplication of A and B is :  " + e);
        System.out.println("Subtraction  of A and B is :  " + g);
    }
}
